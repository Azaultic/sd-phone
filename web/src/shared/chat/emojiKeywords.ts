const stripVS = (e: string): string => e.replace(/пёҸ/g, '');

const NORMALIZED: Record<string, string> = (() => {
    const out: Record<string, string> = {};
    for (const key in EMOJI_KEYWORDS) out[stripVS(key)] = EMOJI_KEYWORDS[key];
    return out;
})();

export function emojiKeywords(emoji: string): string {
    return NORMALIZED[stripVS(emoji)] ?? '';
}

export function emojiName(emoji: string): string {
    const kw = emojiKeywords(emoji);
    if (!kw) return '';
    const phrase = kw.split(' ').slice(0, 2).join(' ');
    return phrase.charAt(0).toUpperCase() + phrase.slice(1);
}
